/**
 * Dictionary — typ szerokiego dictionary (nie literal-narrow).
 *
 * Trick: `typeof pl` po `as const` daje literal types (np. `"Zawsze na miejscu"`),
 * przez co EN nie może zostać przypisany do `Dictionary` ("Always there" ≠ literal PL).
 *
 * `Widen<T>` rekurencyjnie zamienia literal stringi/number na `string`/`number`
 * i zdejmuje `readonly`/`as const` — typ pozostaje strukturalnie identyczny,
 * ale przyjmuje dowolne wartości tego samego kształtu. Dzięki temu EN i PL mają
 * **dokładnie ten sam zestaw kluczy** (gwarancja parytetu) bez problemu z literal types.
 */
type Widen<T> = T extends string
  ? string
  : T extends number
    ? number
    : T extends boolean
      ? boolean
      : T extends readonly (infer U)[]
        ? readonly Widen<U>[]
        : T extends object
          ? { readonly [K in keyof T]: Widen<T[K]> }
          : T;

export type Dictionary = Widen<typeof pl>;
